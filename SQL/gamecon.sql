CREATE TABLE `ACCOUNT` (
  `ACCOUNT_ID` DECIMAL(22,0) NOT NULL,
  `LAST_NAME` VARCHAR(20) NULL DEFAULT NULL,
  `FIRST_NAME` VARCHAR(20) NULL DEFAULT NULL,
  `EMAIL` VARCHAR(50) NULL DEFAULT NULL,
  `PASSWORD` VARCHAR(16) NULL DEFAULT NULL,
  `DATE_OF_BIRTH` DATETIME NULL DEFAULT NULL,
  `PHONE` VARCHAR(20) NULL DEFAULT NULL,
  `ADDRESS` VARCHAR(20) NULL DEFAULT NULL,
  `EMERGENCY_CONTACT` VARCHAR(20) NULL DEFAULT NULL,
  `IS_ADULT` VARCHAR(5) NULL DEFAULT NULL,
  PRIMARY KEY (`ACCOUNT_ID` ASC)
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
CREATE TABLE `DEPARTMENT` (
  `DEPT_ID` DECIMAL(22,0) NOT NULL,
  `NAME` VARCHAR(30) NULL DEFAULT NULL,
  `BANDANA_COLOR` VARCHAR(10) NULL DEFAULT NULL,
  PRIMARY KEY (`DEPT_ID` ASC)
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
CREATE TABLE `HOTEL` (
  `HOTEL_ID` DECIMAL(22,0) NOT NULL,
  `NAME` VARCHAR(30) NULL DEFAULT NULL,
  `ADDRESS` VARCHAR(30) NULL DEFAULT NULL,
  `PHONE` VARCHAR(15) NULL DEFAULT NULL,
  PRIMARY KEY (`HOTEL_ID` ASC)
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
CREATE TABLE `HOTELROOM` (
  `HOTEL_ROOM_ID` DECIMAL(22,0) NOT NULL,
  `HOTEL_ID` DECIMAL(22,0) NULL DEFAULT NULL,
  `ROOM_NUM` DECIMAL(22,0) NULL DEFAULT NULL,
  PRIMARY KEY (`HOTEL_ROOM_ID` ASC),
  CONSTRAINT `HOTELROOM_FK_HOTEL_ID` FOREIGN KEY (`HOTEL_ID`) REFERENCES `HOTEL` (`HOTEL_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
CREATE TABLE `STAFF` (
  `ACCOUNT_ID` DECIMAL(22,0) NULL DEFAULT NULL,
  `DEPT_ID` DECIMAL(22,0) NULL DEFAULT NULL,
  `HOTEL_ROOM_ID` DECIMAL(22,0) NULL DEFAULT NULL,
  `SHIRT_SIZE` VARCHAR(4) NULL DEFAULT NULL,
  CONSTRAINT `STAFF_FK_HOTELROOM_ID` FOREIGN KEY (`HOTEL_ROOM_ID`) REFERENCES `HOTELROOM` (`HOTEL_ROOM_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `STAFF_FK_DEPT_ID` FOREIGN KEY (`DEPT_ID`) REFERENCES `DEPARTMENT` (`DEPT_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `STAFF_FK_ACCOUNT_ID` FOREIGN KEY (`ACCOUNT_ID`) REFERENCES `ACCOUNT` (`ACCOUNT_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
CREATE TABLE `TICKET` (
  `TICKET_ID` DECIMAL(22,0) NOT NULL,
  `ACCOUNT_ID` DECIMAL(22,0) NOT NULL,
  `BADGE_NAME` VARCHAR(20) NULL DEFAULT NULL,
  `PRICE` DECIMAL(22,0) NULL DEFAULT NULL,
  `EXTRAS` VARCHAR(50) NULL DEFAULT NULL,
  `TICKET_TYPE` VARCHAR(20) NULL DEFAULT NULL,
  PRIMARY KEY (`TICKET_ID` ASC),
  CONSTRAINT `TICKET_FK_ACCOUNT_ID` FOREIGN KEY (`ACCOUNT_ID`) REFERENCES `ACCOUNT` (`ACCOUNT_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
CREATE TABLE `ATTENDEE` (
  `ACCOUNT_ID` DECIMAL(22,0) NULL DEFAULT NULL,
  `IS_VOLUNTEER` VARCHAR(5) NULL DEFAULT NULL,
  CONSTRAINT `ATTENDEE_FK_ACCOUNT_ID` FOREIGN KEY (`ACCOUNT_ID`) REFERENCES `ACCOUNT` (`ACCOUNT_ID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) DEFAULT CHARSET=utf8 ENGINE=InnoDB;
